class Solution {
    public static void nextPermutation(int[] nums) {
        int len = nums.length;
        if (nums == null || len == 1){
            return;
        }

        int i = len - 2;
        for (; i >= 0; i --){
            if (nums[i] < nums[i + 1]){
                break;
            }
        }

        if (i >= 0) {
            for (int j = len - 1; j > i; j--) {
                if (nums[j] > nums[i]) {
                    int temp = nums[j];
                    nums[j] = nums[i];
                    nums[i] = temp;
                    break;
                }
            }
        }

        for (int k = i + 1; k < len; k ++){
            for (int l = len - 1; l > k; l --){
                if (nums[l] < nums[l - 1]){
                    int temp = nums[l];
                    nums[l] = nums[l - 1];
                    nums[l - 1] = temp;
                }
            }
        }

    }

    public static void main(String[] args) {
        int[] nums = new int[]{1,2,3};
        nextPermutation(nums);
        for (int i: nums) {
            System.out.print(i);
        }
        System.out.println();

        nums = new int[]{3,2,1};
        nextPermutation(nums);
        for (int i: nums) {
            System.out.print(i);
        }
        System.out.println();

        nums = new int[]{1,3,2};
        nextPermutation(nums);
        for (int i: nums) {
            System.out.print(i);
        }
        System.out.println();
    }
}
